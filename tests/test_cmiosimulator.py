class PagingTracer:
    def __init__(self, simulator):
        self.memory = simulator.memory

    def write_port(self, registers, port, value):
        if port & 0x8002 == 0:
            self.memory.out7ffd(value)

class CMIOSimulatorMEMPTRTest(SkoolKitTestCase):
    simulator_cls = CCMIOSimulator or CMIOSimulator

    def test_instructions(self):
        simulator = self.simulator_cls([0] * 65536, REGISTERS_IN)
        memory = simulator.memory
        reg = simulator.registers
        registers_in = reg[:]
        for opcodes_hex, operation, memptr_out in OPCODES:
            for i in range(0, len(opcodes_hex), 2):
                memory[pc + i // 2] = int(opcodes_hex[i:i + 2], 16)
            memory[sp:sp + 2] = (ret % 256, ret // 256)
            reg[:] = registers_in
            simulator.run()
            self.assertEqual(reg[MEMPTR], memptr_out, f'Failed for {opcodes_hex} {operation}')

    def test_conditionals(self):
        simulator = self.simulator_cls([0] * 65536, REGISTERS_IN)
        memory = simulator.memory
        reg = simulator.registers
        registers_in = reg[:]
        for opcodes_hex, operation, reg_in, memptr_out in CONDITIONALS:
            for i in range(0, len(opcodes_hex), 2):
                memory[pc + i // 2] = int(opcodes_hex[i:i + 2], 16)
            memory[sp:sp + 2] = (ret % 256, ret // 256)
            reg[:] = registers_in
            for r, v in reg_in.items():
                if r == '(HL)':
                    memory[hl] = v
                else:
                    reg[r] = v
            simulator.run()
            self.assertEqual(reg[MEMPTR], memptr_out, f'Failed for {opcodes_hex} {operation}')

    def test_interrupt_im_0(self):
        state = {'iff': 1, 'im': 0, 'tstates': 69886}
        simulator = self.simulator_cls([0] * 65536, state=state)
        simulator.run(0, 56, True)
        self.assertEqual(simulator.registers[MEMPTR], 56)

